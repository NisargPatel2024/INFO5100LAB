    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:
        try {
            String fname = "First Name: " + this.jTextFieldFirstName.getText() + "\n";
            String lname = "Last Name: " + this.jTextFieldLastName.getText() + "\n";
            String gender = "Gender: " + this.buttonGroupGender.getSelection().getActionCommand() + "\n";
            String phoneNumber = "Phone Number: " + this.jTextFieldPhoneNumber.getText() + "\n";
            String email = this.jTextFieldEmail.getText();
            String emailString;

            int Age = Integer.parseInt(this.jTextFieldAge.getText());
            String ageString = "Age: " + Age + "\n";

//          For Terminal output
            System.out.println(this.jTextFieldFirstName.getText());
            System.out.println(this.jTextFieldLastName.getText());
            System.out.println(this.buttonGroupGender.getSelection().getActionCommand());

            System.out.println(this.jTextFieldAge.getText());

            System.out.println(this.jTextFieldPhoneNumber.getText());
            System.out.println(this.jTextFieldEmail.getText());

//          Email validation 
            String emailRegex = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";

            if (!Pattern.matches(emailRegex, email)) {
                System.out.println("Invalid formate of Email.");
                String invaldEmail = "Invalid formate of Email.";
                emailString = "Email:" + invaldEmail;

            } else {
                System.out.println("valid formate of Email.");
                emailString = "Email:" + email;
            }

//          UI output
            String displayMessage = fname + lname + gender + phoneNumber + ageString + emailString;

            if (this.jLabelImagePreview.getIcon() != null) {
                JOptionPane.showMessageDialog(this, displayMessage, "User Information", JOptionPane.INFORMATION_MESSAGE, this.jLabelImagePreview.getIcon());
            } else {
                JOptionPane.showMessageDialog(this, displayMessage);
            }

        } catch (NullPointerException ne) {
            String errorMessage = "You must enter value of First&Last Name and gender";
            System.out.println(errorMessage);
            JOptionPane.showMessageDialog(this, errorMessage);

        } catch (NumberFormatException nfe) {
            String errorMessage = "Enter a valid input in age field";
            System.out.println(nfe);
            JOptionPane.showMessageDialog(this, errorMessage);
        } catch (HeadlessException e) {
            System.out.println("Exception has occured");
            System.out.println(e);
        }

    }//GEN-LAST:event_jButtonSubmitActionPerformed

